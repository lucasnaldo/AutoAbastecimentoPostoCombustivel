    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        float novoValor;
        novoValor = 0;
        
        novoValor = Float.parseFloat(ftfNovoValor.getText());
        if (cbCombustiveis.getSelectedItem() == "Gasolina Comum")
            frameMenu.gasc = novoValor;
        
        else if (cbCombustiveis.getSelectedItem() == "Gasolina Aditivada")
            frameMenu.gasa = novoValor;
        
        else if (cbCombustiveis.getSelectedItem() == "Alcool")
            frameMenu.al = novoValor;
        else
            frameMenu.di = novoValor;
        JOptionPane.showMessageDialog(null,"Valor do combustivel atualizado!");
        limparCamposFrame();
        dispose();
    }
    
    //METODO PARA LIMPAR OS CAMPOS DO FRAME
    public void limparCamposFrame(){
        cbCombustiveis.setSelectedIndex(-1);
        ftfNovoValor.setText("");
    }//GEN-LAST:event_btSalvarActionPerformed
   
    
